package net.consorcio.entidad;


public class InformeTecnico {

		private int codigo;
		private String introduccion;
		private String antecedentes;
		private String analisis;
		private String conclusiones;
		private String recomendaciones;
		private int codigoEstado;
		private String nombreEstado;
		private int codigoUsuario;
		private int codigoRequerimiento;
		public int getCodigo() {
			return codigo;
		}
		public void setCodigo(int codigo) {
			this.codigo = codigo;
		}
		public String getIntroduccion() {
			return introduccion;
		}
		public void setIntroduccion(String introduccion) {
			this.introduccion = introduccion;
		}
		public String getAntecedentes() {
			return antecedentes;
		}
		public void setAntecedentes(String antecedentes) {
			this.antecedentes = antecedentes;
		}
		public String getAnalisis() {
			return analisis;
		}
		public void setAnalisis(String analisis) {
			this.analisis = analisis;
		}
		public String getConclusiones() {
			return conclusiones;
		}
		public void setConclusiones(String conclusiones) {
			this.conclusiones = conclusiones;
		}
		public String getRecomendaciones() {
			return recomendaciones;
		}
		public void setRecomendaciones(String recomendaciones) {
			this.recomendaciones = recomendaciones;
		}
		public int getCodigoEstado() {
			return codigoEstado;
		}
		public void setCodigoEstado(int codigoEstado) {
			this.codigoEstado = codigoEstado;
		}
		public String getNombreEstado() {
			return nombreEstado;
		}
		public void setNombreEstado(String nombreEstado) {
			this.nombreEstado = nombreEstado;
		}
		public int getCodigoUsuario() {
			return codigoUsuario;
		}
		public void setCodigoUsuario(int codigoUsuario) {
			this.codigoUsuario = codigoUsuario;
		}
		public int getCodigoRequerimiento() {
			return codigoRequerimiento;
		}
		public void setCodigoRequerimiento(int codigoRequerimiento) {
			this.codigoRequerimiento = codigoRequerimiento;
		}
		
				
		
}
